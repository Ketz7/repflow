export default function BottomNav() {
  const pathname = usePathname();

  // Hide nav during workout sessions
  if (pathname.startsWith("/session")) return null;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-surface/60 backdrop-blur-2xl border-t border-white/10 shadow-[0_-4px_30px_rgba(0,0,0,0.3)] safe-bottom">
      <div className="flex items-center justify-around h-16 max-w-lg mx-auto">
        {tabs.map((tab) => {
          const isActive =
            tab.href === "/"
              ? pathname === "/"
              : pathname.startsWith(tab.href);

          return (
            <Link
              key={tab.name}
              href={tab.href}
              className={`flex flex-col items-center justify-center gap-0.5 w-16 py-1 transition-colors duration-200 ${
                isActive ? "text-primary" : "text-subtext"
              }`}
            >
              {tab.icon(isActive)}
              <span className="text-[10px] font-medium">{tab.name}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
